# 8. UserDocument — Knowledge Vault (Day 7)
# ===========================================================================
class UserDocument(TimeStampedModel):
    """
    An uploaded document stored in the Knowledge Vault.
    Text is extracted on upload and made available to Jarvis for Q&A.
    Supported formats: PDF, DOCX, PPTX, TXT.
    """

    FILE_TYPE_CHOICES = [
        ('pdf',  'PDF Document'),
        ('docx', 'Word Document'),
        ('pptx', 'PowerPoint'),
        ('txt',  'Text File'),
    ]

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='documents',
    )
    title             = models.CharField(max_length=255, help_text='Display name.')
    original_filename = models.CharField(max_length=255, blank=True, default='')
    file              = models.FileField(upload_to='documents/%Y/%m/')
    file_type         = models.CharField(max_length=10, choices=FILE_TYPE_CHOICES)
    file_size         = models.PositiveIntegerField(default=0, help_text='Bytes.')
    extracted_text    = models.TextField(
        blank=True,
        default='',
        help_text='Extracted plaintext used by AI.',
    )
    is_processed      = models.BooleanField(default=False)

    class Meta:
        verbose_name        = 'User Document'
        verbose_name_plural = 'User Documents'
        ordering            = ['-created_at']

    def __str__(self):
        return f'[{self.user.username}] {self.title}'

    def file_size_display(self):
        s = self.file_size
        if s < 1024:         return f'{s} B'
        if s < 1024 * 1024:  return f'{s / 1024:.1f} KB'
        return f'{s / (1024 * 1024):.1f} MB'

    @property
    def icon(self):
        return {
            'pdf':  'bi-file-earmark-pdf-fill',
            'docx': 'bi-file-earmark-word-fill',
            'pptx': 'bi-file-earmark-ppt-fill',
            'txt':  'bi-file-earmark-text-fill',
        }.get(self.file_type, 'bi-file-earmark-fill')


