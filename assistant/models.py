# 4. Note
# ===========================================================================
class Note(TimeStampedModel):
    """
    A freeform note created by the user, optionally associated with
    a chat session. Supports tagging and pinning.
    """

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='notes',
    )
    session = models.ForeignKey(
        ChatSession,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name='notes',
        help_text='Optional: note created during this chat session.',
    )

    title = models.CharField(
        max_length=255,
        default='Untitled Note',
    )
    content = models.TextField(
        help_text='Main body of the note (supports Markdown).',
    )
    tags = models.CharField(
        max_length=500,
        blank=True,
        default='',
        help_text='Comma-separated tag list, e.g. "work,ideas,urgent".',
    )
    is_pinned = models.BooleanField(
        default=False,
        help_text='Pinned notes appear at the top of the list.',
    )
    is_archived = models.BooleanField(
        default=False,
    )

    class Meta:
        verbose_name        = 'Note'
        verbose_name_plural = 'Notes'
        ordering            = ['-is_pinned', '-updated_at']

    def __str__(self):
        return f'[{self.user.username}] {self.title}'

    def tag_list(self):
        """Return tags as a Python list."""
        return [t.strip() for t in self.tags.split(',') if t.strip()]


