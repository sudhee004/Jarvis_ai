# 9. VoiceSettings — Per-user voice preferences (Day 7)
# ===========================================================================
class VoiceSettings(TimeStampedModel):
    """Stores each user's voice assistant preferences server-side."""

    GENDER_CHOICES = [
        ('female', 'Female'),
        ('male',   'Male'),
    ]
    LANGUAGE_CHOICES = [
        ('en-IN', 'English (India)'),
        ('en-GB', 'English (UK)'),
        ('en-US', 'English (US)'),
        ('kn-IN', 'Kannada'),
        ('mixed', 'Mixed (Kannada + English)'),
    ]

    user         = models.OneToOneField(
        User, on_delete=models.CASCADE, related_name='voice_settings',
    )
    voice_gender = models.CharField(max_length=10, choices=GENDER_CHOICES, default='female')
    language     = models.CharField(max_length=10, choices=LANGUAGE_CHOICES, default='en-IN')
    auto_speak   = models.BooleanField(default=True)
    speech_rate  = models.FloatField(default=1.0, help_text='Range 0.5–2.0')
    speech_pitch = models.FloatField(default=1.1, help_text='Range 0.0–2.0')

    class Meta:
        verbose_name        = 'Voice Settings'
        verbose_name_plural = 'Voice Settings'

    def __str__(self):
        return f'VoiceSettings({self.user.username})'
