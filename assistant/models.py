# 5. Reminder
# ===========================================================================
class Reminder(TimeStampedModel):
    """
    A time-bound reminder that belongs to a user.
    The actual notification dispatch will be handled by Celery (future sprint).
    """

    PRIORITY_CHOICES = [
        ('low',    'Low'),
        ('medium', 'Medium'),
        ('high',   'High'),
        ('urgent', 'Urgent'),
    ]

    STATUS_CHOICES = [
        ('pending',   'Pending'),
        ('sent',      'Sent'),
        ('dismissed', 'Dismissed'),
        ('snoozed',   'Snoozed'),
    ]

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='reminders',
    )
    session = models.ForeignKey(
        ChatSession,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name='reminders',
        help_text='Optional: reminder created during this chat session.',
    )

    title = models.CharField(max_length=255)
    description = models.TextField(
        blank=True,
        default='',
        help_text='Optional additional context for this reminder.',
    )
    remind_at = models.DateTimeField(
        help_text='When to trigger this reminder.',
    )
    priority = models.CharField(
        max_length=10,
        choices=PRIORITY_CHOICES,
        default='medium',
    )
    status = models.CharField(
        max_length=20,
        choices=STATUS_CHOICES,
        default='pending',
    )
    is_recurring = models.BooleanField(
        default=False,
    )
    recurrence_rule = models.CharField(
        max_length=100,
        blank=True,
        default='',
        help_text='iCal RRULE string, e.g. "FREQ=DAILY;INTERVAL=1".',
    )
    snoozed_until = models.DateTimeField(
        null=True,
        blank=True,
        help_text='If snoozed, the new trigger time.',
    )

    class Meta:
        verbose_name        = 'Reminder'
        verbose_name_plural = 'Reminders'
        ordering            = ['remind_at']

    def __str__(self):
        return (
            f'[{self.user.username}] {self.title} '
            f'@ {self.remind_at.strftime("%Y-%m-%d %H:%M")} '
            f'({self.get_status_display()})'
        )

    def is_overdue(self):
        """Return True if the reminder is past its due time and still pending."""
        return self.status == 'pending' and self.remind_at < timezone.now()


