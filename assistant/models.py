"""
Database models for the Jarvis AI Assistant — Day 3.

Model hierarchy:
  User (Django built-in)
    └── UserProfile      — extended profile (OneToOne)
    └── ChatSession      — a named conversation thread
          └── ChatMessage — individual turns in a session
    └── Note             — freeform notes
    └── Reminder         — time-bound reminders
    └── UserMemory       — persistent key-value AI context store
    └── ActivityLog      — immutable audit trail of user actions
"""

from django.contrib.auth.models import User
from django.db import models
from django.utils import timezone


# ---------------------------------------------------------------------------
# Shared abstract base — adds created_at / updated_at to any model
# ---------------------------------------------------------------------------
class TimeStampedModel(models.Model):
    """
    Abstract base class that automatically records creation and
    last-modification timestamps on every subclass.
    """
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        abstract = True


# ===========================================================================
# 1. UserProfile
# ===========================================================================
class UserProfile(TimeStampedModel):
    """
    One-to-one extension of Django's built-in User model.
    Stores application-specific preferences and metadata.
    """

    THEME_CHOICES = [
        ('dark',  'Dark Mode'),
        ('light', 'Light Mode'),
        ('auto',  'Auto (System)'),
    ]

    LANGUAGE_CHOICES = [
        ('en', 'English'),
        ('es', 'Spanish'),
        ('fr', 'French'),
        ('de', 'German'),
        ('hi', 'Hindi'),
    ]

    # Core link
    user = models.OneToOneField(
        User,
        on_delete=models.CASCADE,
        related_name='profile',
        help_text='The Django user this profile belongs to.',
    )

    # Display
    avatar = models.ImageField(
        upload_to='avatars/',
        null=True,
        blank=True,
        help_text='Optional profile picture.',
    )
    bio = models.TextField(
        blank=True,
        default='',
        help_text='Short user biography or tagline.',
    )

    # Preferences
    theme = models.CharField(
        max_length=10,
        choices=THEME_CHOICES,
        default='dark',
    )
    preferred_language = models.CharField(
        max_length=10,
        choices=LANGUAGE_CHOICES,
        default='en',
    )
    timezone = models.CharField(
        max_length=50,
        default='UTC',
        help_text='IANA timezone string, e.g. "Asia/Kolkata".',
    )

    # Feature toggles
    voice_enabled = models.BooleanField(
        default=True,
        help_text='Whether the voice assistant feature is active.',
    )
    notifications_enabled = models.BooleanField(
        default=True,
        help_text='Whether browser/email notifications are enabled.',
    )

    # Stats (denormalised counters for quick display)
    total_messages = models.PositiveIntegerField(default=0)
    total_sessions  = models.PositiveIntegerField(default=0)

    class Meta:
        verbose_name        = 'User Profile'
        verbose_name_plural = 'User Profiles'
        ordering            = ['-created_at']

    def __str__(self):
        return f'Profile({self.user.username})'

    def get_display_name(self):
        """Return full name if available, otherwise username."""
        return self.user.get_full_name() or self.user.username


