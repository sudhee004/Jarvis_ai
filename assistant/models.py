# 7. ActivityLog
# ===========================================================================
class ActivityLog(models.Model):
    """
    Immutable audit trail of significant user actions within the app.
    Records are never updated — only inserted and (soft-)deleted.

    Examples of action values:
      'register', 'login', 'logout',
      'create_note', 'delete_note',
      'create_reminder', 'dismiss_reminder',
      'start_chat', 'send_message',
    """

    ACTION_CHOICES = [
        # Auth
        ('register',          'Registered'),
        ('login',             'Logged In'),
        ('logout',            'Logged Out'),
        # Chat
        ('start_chat',        'Started Chat Session'),
        ('send_message',      'Sent Message'),
        ('archive_chat',      'Archived Chat'),
        # Notes
        ('create_note',       'Created Note'),
        ('update_note',       'Updated Note'),
        ('delete_note',       'Deleted Note'),
        ('pin_note',          'Pinned Note'),
        # Reminders
        ('create_reminder',   'Created Reminder'),
        ('dismiss_reminder',  'Dismissed Reminder'),
        ('snooze_reminder',   'Snoozed Reminder'),
        # Profile
        ('update_profile',    'Updated Profile'),
        ('update_settings',   'Changed Settings'),
        # Other
        ('other',             'Other'),
    ]

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='activity_logs',
        help_text='The user who performed the action.',
    )
    action = models.CharField(
        max_length=40,
        choices=ACTION_CHOICES,
        default='other',
        db_index=True,
    )
    description = models.TextField(
        blank=True,
        default='',
        help_text='Human-readable description of the action.',
    )
    # Optional JSON-compatible metadata (e.g. {"note_id": 42})
    metadata = models.JSONField(
        default=dict,
        blank=True,
        help_text='Arbitrary JSON payload for additional context.',
    )
    ip_address = models.GenericIPAddressField(
        null=True,
        blank=True,
        help_text='Client IP address at the time of the action.',
    )
    user_agent = models.TextField(
        blank=True,
        default='',
        help_text='Browser / client User-Agent string.',
    )
    created_at = models.DateTimeField(
        auto_now_add=True,
        db_index=True,
    )

    class Meta:
        verbose_name        = 'Activity Log'
        verbose_name_plural = 'Activity Logs'
        ordering            = ['-created_at']

    def __str__(self):
        ts = self.created_at.strftime('%Y-%m-%d %H:%M')
        return f'[{ts}] {self.user.username} → {self.get_action_display()}'


