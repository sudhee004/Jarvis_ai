# 6. UserMemory
# ===========================================================================
class UserMemory(TimeStampedModel):
    """
    Persistent key-value store for AI context about a user.
    Jarvis uses this to personalise responses across sessions.

    Examples:
      key='user_name_preference', value='Call me Alex'
      key='dietary_preference',   value='vegetarian'
      key='work_role',            value='software engineer'
    """

    CATEGORY_CHOICES = [
        ('preference', 'Preference'),
        ('fact',       'Personal Fact'),
        ('goal',       'Goal'),
        ('context',    'Context'),
        ('other',      'Other'),
    ]

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='memories',
    )
    key = models.CharField(
        max_length=120,
        help_text='Short snake_case identifier, e.g. "preferred_name".',
    )
    value = models.TextField(
        help_text='The remembered value or fact.',
    )
    category = models.CharField(
        max_length=20,
        choices=CATEGORY_CHOICES,
        default='other',
    )
    confidence = models.FloatField(
        default=1.0,
        help_text='AI confidence score (0.0–1.0) for this memory entry.',
    )
    is_active = models.BooleanField(
        default=True,
        help_text='Inactive memories are ignored by the AI but kept for audit.',
    )
    source_session = models.ForeignKey(
        ChatSession,
        on_delete=models.SET_NULL,
        null=True,
        blank=True,
        related_name='memories',
        help_text='Session in which this memory was first recorded.',
    )

    class Meta:
        verbose_name        = 'User Memory'
        verbose_name_plural = 'User Memories'
        ordering            = ['-updated_at']
        # Each user can have at most one active entry per key
        unique_together = [('user', 'key')]

    def __str__(self):
        active_label = '' if self.is_active else ' [inactive]'
        return f'[{self.user.username}] {self.key}: {self.value[:60]}{active_label}'


