# 2. ChatSession
# ===========================================================================
class ChatSession(TimeStampedModel):
    """
    A named conversation thread between a user and Jarvis.
    Each session groups a sequence of ChatMessages.
    """

    STATUS_CHOICES = [
        ('active',   'Active'),
        ('archived', 'Archived'),
        ('deleted',  'Deleted'),
    ]

    user = models.ForeignKey(
        User,
        on_delete=models.CASCADE,
        related_name='chat_sessions',
        help_text='Owner of this chat session.',
    )
    title = models.CharField(
        max_length=255,
        default='New Chat',
        help_text='Human-readable title for the session.',
    )
    status = models.CharField(
        max_length=20,
        choices=STATUS_CHOICES,
        default='active',
    )
    # Cached summary generated by AI (populated in a later sprint)
    summary = models.TextField(
        blank=True,
        default='',
        help_text='Auto-generated summary of the conversation.',
    )
    # Total tokens used — for future API cost tracking
    total_tokens = models.PositiveIntegerField(
        default=0,
        help_text='Cumulative token count for this session.',
    )
    last_message_at = models.DateTimeField(
        null=True,
        blank=True,
        help_text='Timestamp of the most recent message.',
    )

    class Meta:
        verbose_name        = 'Chat Session'
        verbose_name_plural = 'Chat Sessions'
        ordering            = ['-last_message_at', '-created_at']

    def __str__(self):
        return f'[{self.user.username}] {self.title} ({self.get_status_display()})'

    def message_count(self):
        """Return the number of messages in this session."""
        return self.messages.count()


# ===========================================================================
# 3. ChatMessage
# ===========================================================================
class ChatMessage(models.Model):
    """
    A single turn (user or assistant) within a ChatSession.
    Not time-stamped via TimeStampedModel so we use a single
    created_at only — messages are immutable once written.
    """

    ROLE_CHOICES = [
        ('user',      'User'),
        ('assistant', 'Jarvis AI'),
        ('system',    'System'),
    ]

    session = models.ForeignKey(
        ChatSession,
        on_delete=models.CASCADE,
        related_name='messages',
        help_text='Parent session this message belongs to.',
    )
    role = models.CharField(
        max_length=20,
        choices=ROLE_CHOICES,
        default='user',
        help_text='Who sent this message.',
    )
    content = models.TextField(
        help_text='Raw text content of the message.',
    )
    # Token count for this individual message
    token_count = models.PositiveIntegerField(
        default=0,
        help_text='Number of tokens in this message.',
    )
    # Was this message flagged as sensitive/error?
    is_flagged = models.BooleanField(
        default=False,
        help_text='Flagged for review by system or moderator.',
    )
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        verbose_name        = 'Chat Message'
        verbose_name_plural = 'Chat Messages'
        ordering            = ['created_at']

    def __str__(self):
        preview = self.content[:60] + '…' if len(self.content) > 60 else self.content
        return f'[{self.get_role_display()}] {preview}'


